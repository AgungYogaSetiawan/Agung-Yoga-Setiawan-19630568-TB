    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        try{
            String date = "yyyy-MM-dd";
            SimpleDateFormat format = new SimpleDateFormat(date);
            String tanggal = String.valueOf(format.format(jDateChooser1.getDate()));
            String SQL = "UPDATE tb_brg_keluar SET tanggal_keluar = '"+tanggal+"', id_brg_keluar = '"+txtBrgK.getText()+"', id_barang = '"+txtID.getText()+"',nama_barang= '"+txtNama.getText()+"',gudang = '"+jComboBox3.getSelectedItem()+"',kategori = '"+jComboBox2.getSelectedItem()+"',tujuan= '"+txtTujuan.getText()+"',jumlah= '"+txtJumlah.getText()+"',keterangan= '"+jTextArea1.getText()+"' WHERE id_brg_keluar = '"+txtBrgK.getText()+"'";
            Connection conn=(Connection)config.getkoneksiDB();
            PreparedStatement st=conn.prepareStatement(SQL);
            st.executeUpdate();
            new config().settingKolomTabel(tblBrgKlr, Kolom);
            new config().tampilIsiTabel(tblBrgKlr, sql, Kolom);
            new config().settingLebarKolomTabel(tblBrgKlr, lebar);
            JOptionPane.showMessageDialog(null, "Data Berhasil diubah");
        } catch (Exception e){
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

