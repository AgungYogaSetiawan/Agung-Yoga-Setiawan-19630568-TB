    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String date = "yyyy-MM-dd";
        SimpleDateFormat format = new SimpleDateFormat(date);
        String tanggal = String.valueOf(format.format(jDateChooser1.getDate()));
        try {
            if(jDateChooser1.getDate().equals("")){
                JOptionPane.showMessageDialog(null, "Mohon Masukan Tanggal");
                txtID.requestFocus();
            } else if (txtBrgK.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Mohon Masukan ID Barang Keluar");
                txtNama.requestFocus();
            } else if (txtID.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Mohon Masukan ID Barang");
                txtJumlah.requestFocus();
            } else if (txtNama.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Mohon Masukan Nama Barang");
                txtNama.requestFocus();
            } else if (jComboBox3.getSelectedItem().equals("")){
                JOptionPane.showMessageDialog(null, "Mohon Masukan Nama Gudang");
                txtNama.requestFocus();
            } else if (jComboBox2.getSelectedItem().equals("")){
                JOptionPane.showMessageDialog(null, "Mohon Masukan Nama Kategori");
                txtNama.requestFocus();
            } else if (txtTujuan.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Mohon Masukan Tujuan Pengiriman");
                txtNama.requestFocus();
            } else if (txtJumlah.getText().trim().equals("")){
                JOptionPane.showMessageDialog(null, "Mohon Masukan Jumlah Barang");
                txtNama.requestFocus();
            } else {
                String[] field = {"tanggal_keluar","id_brg_keluar","id_barang","nama_barang","gudang","kategori","tujuan","jumlah","keterangan"};
                String[] isi = {tanggal,txtBrgK.getText(),txtID.getText(),txtNama.getText(),(String) jComboBox3.getSelectedItem(),(String) jComboBox2.getSelectedItem(),txtTujuan.getText(),txtJumlah.getText(),jTextArea1.getText()};
                new config().Simpan("tb_brg_keluar", field, isi);
                new config().settingKolomTabel(tblBrgKlr, Kolom);
                new config().tampilIsiTabel(tblBrgKlr, sql, Kolom);
                new config().settingLebarKolomTabel(tblBrgKlr, lebar);
            }
        } catch (Exception e){
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

